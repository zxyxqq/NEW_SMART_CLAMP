#include "int.h"



/*-------------------------------------------------
 *  函数名INT_Init
 *	功能：  INT初始化
 *  输入：  无
 *  输出：  无
 --------------------------------------------------*/	
void INT_Init(void)
{
	TRISA |= 0B00010000;
}
/*-------------------------------------------------
 *  函数名INT_Read
 *	功能：  读取READ的值
 *  输入：  无
 *  输出：  无
 --------------------------------------------------*/	
void INT_Read(void)
{
	SYS.INT_Value = PA4;
}
/*-------------------------------------------------
 *  函数名INT_Task
 *	功能：  读取READ的值
 *  输入：  无
 *  输出：  无
 --------------------------------------------------*/	
void INT_Task(void)
{
	INT_Read();
}